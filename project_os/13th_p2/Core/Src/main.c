void LcdTask(void const * argument)
{
  for(;;)
  {
		lcd_main();
		pwm_main();
		//osDelay(10);
  }
}

void KeyTask(void const * argument)
{
  for(;;)
  {
		taskENTER_CRITICAL();
		key_main();
		ee_write();
		taskEXIT_CRITICAL();
		osDelay(5);
  }
}

void LedTask(void const * argument)
{
  for(;;)
  {
		led_main();
		osDelay(100);
  }
}


void RtcTask(void const * argument)
{
	char rtct[25];
	RTC_TimeTypeDef rtc_time;
	RTC_DateTypeDef rtc_date;
  for(;;)
  {
		taskENTER_CRITICAL();
		HAL_RTC_GetDate(&hrtc,&rtc_date,RTC_FORMAT_BIN);
		HAL_RTC_GetTime(&hrtc,&rtc_time,RTC_FORMAT_BIN);
		sprintf(rtct,"    %02d:%02d:%02d   ",(uint8_t)rtc_time.Hours,(uint8_t)rtc_time.Minutes,(uint8_t)rtc_time.Seconds);
		LCD_DisplayStringLine(Line9,(uint8_t*)rtct);
		taskEXIT_CRITICAL();
  }
}


